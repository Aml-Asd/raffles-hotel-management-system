    // Helper to get ID for default role/department insertion, useful for setting up FKs
    private static int getEntityIdByName(Statement stmt, String tableName, String nameColumn, String nameValue, String idColumn) throws SQLException {
        // Using PreparedStatement within this helper for safety, even though called from a Statement context
        String sql = "SELECT " + idColumn + " FROM " + tableName + " WHERE " + nameColumn + " = ?";
        try (PreparedStatement pstmt = stmt.getConnection().prepareStatement(sql)) {
            pstmt.setString(1, nameValue);
            try (ResultSet rs = pstmt.executeQuery()) {
                if (rs.next()) {
                    return rs.getInt(idColumn);
                }
            }
        }
        return -1; // Indicates not found
    }

