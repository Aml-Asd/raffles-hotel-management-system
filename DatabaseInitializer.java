    private static void insertRoleIfNotExists(Statement stmt, String roleName, String description, String permissions) throws SQLException {
        // Use PreparedStatement for safety against SQL injection, though less critical for hardcoded values
        String checkSql = "SELECT COUNT(*) FROM Role WHERE RoleName = ?";
        boolean exists = false;
        try (PreparedStatement checkPstmt = stmt.getConnection().prepareStatement(checkSql)) {
            checkPstmt.setString(1, roleName);
            try (ResultSet rs = checkPstmt.executeQuery()) {
                if (rs.next() && rs.getInt(1) > 0) {
                    exists = true;
                }
            }
        }

        if (!exists) {
            String insertSql = "INSERT INTO Role (RoleName, Description, Permissions) VALUES (?, ?, ?)";
            try (PreparedStatement insertPstmt = stmt.getConnection().prepareStatement(insertSql)) {
                insertPstmt.setString(1, roleName);
                insertPstmt.setString(2, description);
                insertPstmt.setString(3, permissions);
                insertPstmt.executeUpdate();
                System.out.println("Inserted default role: " + roleName);
            }
        }
    }

